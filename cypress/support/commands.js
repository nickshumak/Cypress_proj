Cypress.Commands.add('login', (email, pass) => {
    cy.get('#delivery_manager_email')
        .type(email, { force: true });
    cy.get('#delivery_manager_password')
        .type(pass, { force: true });
    cy.get('#delivery_manager_submit_action').click();
})
